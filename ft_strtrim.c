#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*new_str;
	size_t	start;
	size_t	end;

	if (s1 == NULL)
		return (NULL);
	if (set == NULL)
		return (ft_strdup(s1));
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end && s1[end - 1] && ft_strchr(set, s1[end - 1]))
		end--;
	new_str = (char *)malloc(sizeof(char) * (end - start) + 1);
	if (!new_str)
		return (NULL);
	ft_strlcpy(new_str, &s1[start], (end - start) + 1);
	return (new_str);
}
